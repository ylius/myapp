package com.example.entities;

import java.io.Serializable;

// To send an object through Intend, the object's class should implement Serializable interface
public class User implements Serializable {
    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}